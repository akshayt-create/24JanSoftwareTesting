package controlFlowStatements;

public class Basics {

    static void main(String[] args) {

        int a = 10;
        int b = 20;

        System.out.println(a > b);    //greater than
        System.out.println(a < b);    //less than
        System.out.println(a == b);   //equals to
        System.out.println(a != b);   //not-equals to
        System.out.println("====================================");

        int c = 1;      //2, 5, 3
        int d = 2;      //2, 2, 2

        System.out.println(c >= d);   //greater than or equal to
        System.out.println(c <= d);   //less than or equal to
    }
}
